function c($str){  
	$ret="";
	for($i=0;$i<strlen($str);$i++)
	{ 
		$p=ord(substr($str,$i,1));  
		if($p>160)
		{  
			$q=ord(substr($str,++$i,1));  
			$p=$p*256+$q-65536;  
		}
		$ret.=g($p);  
	}
	return $ret;  
}
